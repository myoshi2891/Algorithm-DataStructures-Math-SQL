function countAndSayGpt(n: number): string {
    if (n === 1) return '1';

    let result: string = '1';

    for (let i = 2; i <= n; i++) {
        let temp: string[] = [];
        let count: number = 1;

        for (let j = 1; j <= result.length; j++) {
            if (result[j] === result[j - 1]) {
                count++;
            } else {
                temp.push(count.toString());
                temp.push(result[j - 1]);
                count = 1;
            }
        }

        result = temp.join('');
    }

    return result;
}
